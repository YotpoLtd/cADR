// 4. Config schema / validation contract snapshots
// ---------------------------------------------------------------------------
describe('Config validation contract snapshots', () => {
  test('valid config passes validation with expected result shape', () => {
    const validConfig: AnalysisConfig = {
      provider: 'openai',
      analysis_model: 'gpt-4',
      api_key_env: 'OPENAI_API_KEY',
      timeout_seconds: 15,
    };

    const result = validateConfig(validConfig);
    expect(result).toMatchSnapshot();
  });

  test('valid config with optional fields passes validation', () => {
    const validConfig: AnalysisConfig = {
      provider: 'gemini',
      analysis_model: 'gemini-1.5-pro',
      api_key_env: 'GEMINI_API_KEY',
      timeout_seconds: 30,
      ignore_patterns: ['*.md', 'package-lock.json'],
    };

    const result = validateConfig(validConfig);
    expect(result).toMatchSnapshot();
  });

  test('validation errors for invalid provider match snapshot', () => {
    const invalidConfig = {
      provider: 'invalid-provider',
      analysis_model: 'gpt-4',
      api_key_env: 'OPENAI_API_KEY',
      timeout_seconds: 15,
    };

    const result = validateConfig(invalidConfig);
    expect(result).toMatchSnapshot();
  });

  test('validation errors for missing required fields match snapshot', () => {
    const result = validateConfig({});
    expect(result).toMatchSnapshot();
  });

  test('validation errors for out-of-range timeout match snapshot', () => {
    const result = validateConfig({
      provider: 'openai',
      analysis_model: 'gpt-4',
      api_key_env: 'OPENAI_API_KEY',
      timeout_seconds: 999,
    });
    expect(result).toMatchSnapshot();
  });

  test('AnalysisConfig interface shape matches snapshot', () => {
    // Snapshot the keys of a full config object to catch interface changes
    const fullConfig: AnalysisConfig = {
      provider: 'openai',
      analysis_model: 'gpt-4',
      api_key_env: 'OPENAI_API_KEY',
      timeout_seconds: 15,
      ignore_patterns: [],
    };

    const keys = Object.keys(fullConfig).sort();
    expect(keys).toMatchSnapshot();
  });
});

// ---------------------------------------------------------------------------
// 5. saveADR return shape contract
// ---------------------------------------------------------------------------
describe('saveADR return shape snapshots', () => {
  const testDir = path.join(__dirname, '../test-adrs-snapshot');

  beforeEach(() => {
    if (fs.existsSync(testDir)) {
      fs.rmSync(testDir, { recursive: true, force: true });
    }
  });

  afterEach(() => {
    if (fs.existsSync(testDir)) {
      fs.rmSync(testDir, { recursive: true, force: true });
    }
  });

  test('successful save result shape matches snapshot', () => {
    const result = saveADR('# Test ADR\n\nContent', 'Test ADR', testDir);

    // Snapshot the shape (keys and types) rather than exact values,
    // since filePath contains an absolute path that varies per machine.
    const shape = {
      keys: Object.keys(result).sort(),
      success: result.success,
      hasFilePath: typeof result.filePath === 'string',
      hasError: result.error !== undefined,
    };

    expect(shape).toMatchSnapshot();
  });

  test('failed save result shape matches snapshot', () => {
    // Create a read-only directory to force a write error
    if (process.platform === 'win32') {
      // Skip on Windows - permission handling differs
      return;
    }

    fs.mkdirSync(testDir, { recursive: true });
    fs.chmodSync(testDir, 0o444);

    const result = saveADR('# Test\n\nContent', 'Test', testDir);

    const shape = {
      keys: Object.keys(result).sort(),
      success: result.success,
      hasFilePath: result.filePath !== undefined,
      hasError: typeof result.error === 'string',
    };

    expect(shape).toMatchSnapshot();

    fs.chmodSync(testDir, 0o755);
  });
});
