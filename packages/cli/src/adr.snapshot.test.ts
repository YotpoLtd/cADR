// 3. ADR filename generation contract
// ---------------------------------------------------------------------------
describe('ADR filename generation snapshots', () => {
  test('generateADRFilename output matches snapshot for typical inputs', () => {
    const cases = [
      { number: 1, title: 'Use PostgreSQL for Storage' },
      { number: 42, title: 'Adopt React 18' },
      { number: 100, title: 'Replace REST with gRPC' },
      { number: 5, title: 'Switch to OAuth 2.0!' },
      { number: 9999, title: 'Final Decision' },
    ];

    const results = cases.map(({ number, title }) => ({
      input: { number, title },
      output: generateADRFilename(number, title),
    }));

    expect(results).toMatchSnapshot();
  });

  test('titleToSlug output matches snapshot for edge cases', () => {
    const inputs = [
      'Simple Title',
      'API v2.0: New Endpoints!',
      '---Leading Hyphens---',
      'Multiple   Spaces   Here',
      '',
      '!!!',
      'CamelCase And snake_case Mixed',
    ];

    const results = inputs.map((input) => ({
      input,
      output: titleToSlug(input),
    }));

    expect(results).toMatchSnapshot();
  });
});

