/**
 * Snapshot / Contract Tests
 *
 * These tests verify the stability of external interfaces and output formats.
 * They should fail when output structure changes, forcing explicit approval
 * via `jest --updateSnapshot`.
 */

import { showHelp, showVersion } from './index';
import { ANALYSIS_PROMPT_V1, GENERATION_PROMPT_V1, formatPrompt, formatGenerationPrompt } from './llm/prompts';
import { validateConfig, type AnalysisConfig } from './config';
import { generateADRFilename, titleToSlug, saveADR } from './adr/adr';
import * as fs from 'fs';
import * as path from 'path';

// ---------------------------------------------------------------------------
// 1. CLI help output snapshot
// ---------------------------------------------------------------------------
describe('CLI interface snapshots', () => {
  let stdoutSpy: jest.SpyInstance;

  beforeEach(() => {
    stdoutSpy = jest.spyOn(process.stdout, 'write').mockImplementation();
  });

  afterEach(() => {
    stdoutSpy.mockRestore();
  });

  test('help output matches snapshot', () => {
    showHelp();
    const helpText = stdoutSpy.mock.calls[0][0] as string;
    expect(helpText).toMatchSnapshot();
  });

  test('version output matches snapshot', () => {
    showVersion();
    const versionText = stdoutSpy.mock.calls[0][0] as string;
    expect(versionText).toMatchSnapshot();
  });
});

// ---------------------------------------------------------------------------
// 2. ADR template / prompt structure snapshots
//
//    The actual ADR content is generated by an LLM so we cannot snapshot it
//    directly. Instead we snapshot the prompt templates that define the
//    contract between cADR and the LLM, and the formatted prompts produced
//    with known inputs.
// ---------------------------------------------------------------------------
describe('LLM prompt template snapshots', () => {
  test('ANALYSIS_PROMPT_V1 matches snapshot', () => {
    expect(ANALYSIS_PROMPT_V1).toMatchSnapshot();
  });

  test('GENERATION_PROMPT_V1 matches snapshot', () => {
    expect(GENERATION_PROMPT_V1).toMatchSnapshot();
  });

  test('formatted analysis prompt with known input matches snapshot', () => {
    const formatted = formatPrompt(ANALYSIS_PROMPT_V1, {
      file_paths: ['src/auth/login.ts', 'src/auth/oauth.ts'],
      diff_content: '--- a/src/auth/login.ts\n+++ b/src/auth/login.ts\n@@ -1,3 +1,5 @@\n+import { OAuth2Client } from "google-auth-library";\n',
    });
    expect(formatted).toMatchSnapshot();
  });

  test('formatted generation prompt with known input matches snapshot', () => {
    // Pin the date so the snapshot is deterministic
    const realDate = Date;
    const mockDate = new Date('2025-06-15T00:00:00Z');
    jest.spyOn(global, 'Date').mockImplementation(() => mockDate as unknown as Date);

    const formatted = formatGenerationPrompt({
      file_paths: ['src/auth/login.ts', 'src/auth/oauth.ts'],
      diff_content: '--- a/src/auth/login.ts\n+++ b/src/auth/login.ts\n@@ -1,3 +1,5 @@\n+import { OAuth2Client } from "google-auth-library";\n',
    });
    expect(formatted).toMatchSnapshot();

    jest.restoreAllMocks();
  });
});

